    <img src="sumbar/6.jpg" style="width:40%">
    <p>Di Sumatera Barat, terdapat sebuah pegunungan tinggi yang puncaknya yang sudah dikenal oleh dunia dan wisatawan mancanegara, puncak tersebut dinamakan Puncak Lawang yang ditempatkan sekitar 20 kilo meter dari kota Bukit Tinggi. Disini, para pengunjung bisa menikmati pemandangan kota Bukit Tinggi dari Puncak Lawang, anda bisa melihat hutan yang melihat kota Bukit Tinggi dan Danau Maninjau dari ketinggian<br></p>

 <h3>7. Jam Gadang</h3>
    <img src="sumbar/7.jpg" style="width:40%">
