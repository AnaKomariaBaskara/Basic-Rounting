 <h3>4. Pulau Pagang</h3>
    <img src="sumbar/4.jpg" style="width:40%">
    <p>Naik perahu ke Pulau Pagang di mana pasir putih dan air yang jernih siap menyambut Anda. Airnya begitu biru dan jernih dan pastinya Anda akan langsung tergoda melompat dari perahu meski belum menginjakkan kaki di pulau ini. Pemandangan bawah lautnya juga dipenuhi dengan ikan berwarna-warni, menjadikannya destinasi yang pas buat fans snorkeling!<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Cara ke Sana: Dari Padang pergilah ke Pelabuhan Bungus (10 km) di mana Anda harus mengunjungi beberapa penyewaan perahu dan mencari harga yang pas untuk Anda (biasanya mulai dari Rp250.000 per orang). Setelahnya, perlu waktu sekitar 50 menit untuk sampai di pulau tersebut menggunakan speedboat.</div>

 <h3>5. Kebun Teh Alahan Panjang</h3>
    <img src="sumbar/5.jpg" style="width:40%">
    <p>Anda pasti akan menyukai bentang alam hijau dan udara sejuk yang dicampur dengan aroma teh. Di tempat ini, Anda juga bisa melihat petani-petani teh memetik daun-daun terbaik.Yang paling membuat menarik destinasi ini adalah adanya dua danau cantik sekaligus! Danau Kembar, sama seperti kebun tehnya sendiri, terletak di dataran tinggi, menjadikannya tempat yang sangat pas untuk mengasah kemampuan fotografi<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Air Batumbuk, Gunung Talang Solok, Sumatera Barat</div>

