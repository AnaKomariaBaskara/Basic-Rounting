 <h3>8. Pemutusan Pantai</h3>
    <img src="sumbar/8.jpg" style="width:40%">
    <p>Tempat ini bisa menjadi surga bagi wisatawan dan penyelam, karena disini anda bisa melihat dan menikmati tawaran besar terumbu karang. Di sekitar Pantai Pemutusan ada beberapa pulau kecil yang bisa dikunjungi menggunakan perahu dengan keindahan bawah lautnya sangat indah. Ada juga hotel dan resor di sekitaran Pantai Pemutusan yang bisa digunakan para pengunjung untuk beristirahat. Kemajuan di bidang pariwisata dan keindahan alam inilah yang membuat Pantai Pemutusan sangat dikenal di mancanegara.<br></p>

