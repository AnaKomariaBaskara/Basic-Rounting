  <h3>1. Istana Siak</h3>
    <img src="riau/1.jpg" style="width:25%">
    <p>Bangunan yang mengadopsi gaya arsitektur Eropa, Arab dan Melayu ini dibangun pada tahun 1889 oleh Sultan Syarif Hasyim Abdul Jalil Syaifuddin. Di sana Kamu bisa melihat beberapa koleksi kerajaan patung perunggu Ratu Wilhelmina dari Belanda dan patung Sultan Syarif Hasyim I yang terbuat dari batu pualam berhiaskan berlian. Tempat peninggalan Kesultanan Siak, kerajaan terbesar di Riau ini juga menjadi salah satu wisata sejarah Pekanbaru lainnya yang paling sering dikunjungi wisatawan. Para pengunjung dapat melihat-lihat peninggalan yang masih ada sehingga mereka tidak hanya terhubung datang, namun juga mendapatkan edukasi tentang sejarah Provinsi Riau<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Jl. Sultan Syarif Kasim, Kecamatan Kampung Dalam, Kabupaten Siak<br>
      Jam Operasional: 08.00-18.00 WIB</div>
        
 <h3>2. Rumah Singgah Tuan Kadi</h3>
    <img src="riau/2.jpg" style="width:40%">
    <p>Menjadi salah satu peninggalan sejarah milik Sultan Kerajaan Siak Sri Indrapura, Rumah Singgah Tuan Kadi terkenal sebagai tempat wisata sejarah Pekanbaru. Tempat wisata sejarah ini menjadi ramai juga karena faktor tempat yang berhubungan dengan Sungai Siak.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Kecamatan Kampung Bandar, Kabupaten Senapelan, Pekanbaru</div>

 <h3>3. Asia Farm</h3>
    <img src="riau/3.jpeg" style="width:40%">
    <p>Asia Farm merupakan tempat wisata yang lagi hits di Pekanbaru. Tak heran jika ada banyak keluarga atau anak-anak muda yang berkunjung ke sini. Berlokasi di Jalan Badak Ujung, Kota Pekanbaru, Riau, Asia Farm menawarkan berbagai wahana hiburan dan edukasi. Untuk wahana hiburannya, Asia Farm memang menonjolkan spot-spot instagramable-nya. Sehingga, pengunjung bisa memilih latar berfoto yang bisa disesuaikan dengan keinginan masing-masing.<br>
      Pengunjung bisa memilih berfoto dengan latar bangunan atau landmark bergaya Belanda, Inggris, dan Jepang. Ada juga wahana waterpark dan kulineran yang bisa pengunjung temukan di sini.Untuk wahana edukasinya, Asia Farm punya wahana bermain sambil belajar di bidang pertanian dan peternakan. Hewan-hewan jinak yang ada di Asia Farm juga sudah divaksin dan dirawat dengan baik. Sehingga, pengunjung bisa bebas bermain dan berinteraksi dengan mereka.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Harga tiket masuk Asia Farm Pekanbaru cukup terjangkau. Tiap orangnya hanya perlu membayar Rp 25.000 saja.<br>
      Untuk jam operasionalnya dimulai pukul 09.00 - 18.00 WIB setiap hari Senin - Jumat.<br>
      setiap hari Sabtu - Minggu jam operasionalnya dimulai pukul 08.00 - 18.00 WIB.</div>

 <h3>4. Taman Rekreasi Alam Mayang</h3>
    <img src="riau/4.jpg" style="width:40%">
    <p>Taman rekreasi tebuka ini bisa Kamu jadikan tempat bermain yang asik bersama keluarga. Udara segar pun bisa Kamu nikmati di sini karena arena ini dikelilingi oleh perbukitan. Kamu yang hobi mancing juga bisa mancing di sana. Menjunjung konsep alam, Taman Wisata Alam Mayang adalah tujuan wisata Pekanbaru yang cocok untuk berkumpul bersama keluarga di akhir pekan. Kegiatan yang ditawarkan bermacam-macam, mulai dari tempat memancing, outbond , area mandi bola, tempat bermain anak-anak, dan lain-lain.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Jl. H. Imam Munandar, Taman Alam Mayang, Tenayan Raya, Kota Pekanbaru<br>
      Jam Operasional: 07.00-18.00 WIB (Setiap Hari)</div>

 <h3>5. Taman Wisata Go Green Sei Pinang</h3>
    <img src="riau/5.jpg" style="width:40%">
    <p>Destinasi Wisata Kabupaten Kampar semakin menggeliat. Aneka wisata bermunculan dan berkembang dengan dahsyatnya, serta siap memanjakan pengunjung/wisatawan. Seperti halnya Taman Agrowisata Go Green yang berlokasi di Desa Sungai Pinang Kecamatan Tambang Kabupaten Kampar, tepatnya masuk Jalan PT. Harvenia Kampar Lestari (HKL) sekira 1 km dari simpang PT HKL.<br>
      Meskipun wisata yang tergolong baru di Kampar, namun Taman Agrowisata Go Green menyediakan berbagai wahana hiburan yang siap memanjakan pengunjung/wisatawan, serta menyediakan wisata edukasi di bidang pertanian, perkebunan, dan perikanan. Di samping menghibur dan memberikan rasa kenyamanan pengunjung/wisatawan, Taman Agrowisa juga diberikan ilmu dan pemahaman terkait bercocok tanam dan budidaya ikan.<br></p>

 <h3>6. Ulu Kasok, Raja Ampat</h3>
    <img src="riau/6.jpg" style="width:40%">
    <p>Ulu Kasok memang bukan Raja Ampat, tetapi destinasi wisata yang satu ini menawarkan keindahan layaknya situs wisata populer tersebut dengan gugusan pulau yang dikelilingi oleh air. Bedanya, jika pulau-pulau di Raja Ampat dikelilingi oleh laut, maka di Ulu Kasok Anda akan menemukan pulau-pulau kecil yang dikelilingi air danau. Dari jauh, Ulu Kasok sangat mirip dengan Raja Ampat yang penuh pulau karang berbalut lautan biru.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Terletak di Desa Pulau Gadang, Kecamatan XIII Koto Kampar, Kabupaten Kampar, Riau</div>

 <h3>7.  Kawah Biru</h3>
    <img src="riau/7.jpg" style="width:40%">
    <p>Kawah Biru yang terletak di Desa Tanah Merah ini sebenarnya adalah lokasi bekas galian yang terisi air hujan sehingga membentuk kawah.
      Warna biru yang indah pada objek wisata Pekanbaru ini disebabkan oleh galian pasir berwarna putih bening sehingga warna air terlihat jelas berwarna biru.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Desa Tanah Merah, Kecamatan Siak Hulu, Kabupaten Kampar</div>

 <h3>8. Anjungan Seni Idrus Tintin</h3>
    <img src="riau/icon.jpg" style="width:40%">
    <p>Berdiri megah di kompleks Bandar Serai Pekanbaru, anjungan Seni Idrus Tintin merupakan tempat yang sering dipakai untuk menampilkan pertunjukan seni. Para wisatawan dapat berfoto-foto atau hanya sekadar duduk santai menikmati keindahan arsitektur yang ditawarkan tempat wisata di kota Pekanbaru satu ini.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Kompleks Bandar Seni Raja Ali Haji (Bandar Serai), Pekanbaru.<br>
      Jam Operasional: 09.00-17.00</div>

 <h3>9. Arboretum Universitas Riau</h3>
    <img src="riau/9.jpg" style="width:40%">
    <p>Baru mendengar kata ‘arboretum’? Arboretum adalah hutan sekunder, dan Arboretum Universitas Riau adalah salah satunya yang masih tersisa di Pekanbaru. Tak hanya digunakan sebagai tempat yang bertujuan untuk masalah penelitian dan pendidikan, tempat ini sekarang lebih sering digunakan kawanan muda untuk dijadikan spot foto terbaik.<br></p>

 <h3>10. Sky Garden Lounge and Resto</h3>
    <img src="riau/10.jpg" style="width:40%">
    <p>Sky Garden Lounge and Resto menjadi salah satu Cafe romantis di Pekanbaru yang patut banget kamu kunjungi, tempat nongkrong yang menyuguhkan pemandangan kota Pekanbaru yang megah ini dapat kamu temukan di tempat ini.<br>
      Di tempat ini kamu juga akan di suguhkan dengan berbagai kuliner khas Amerika, Asia dan Kuliner dari Nusantara, Tempat ini sangat di rekomendasiin bagi kamu yang bingung dalam memilih tempat nongkrong yang asik plus seru dan Romantis yang ada di kota Pekanbaru.<br></p>
      <div class="w3-panel w3-leftbar w3-light-grey">Lokasi : Sky Garden Lounge and Resto berada di Jl. Ahmad Yani no. 81 | The Peak Pekanbaru</div>
  
