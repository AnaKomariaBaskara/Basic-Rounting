    <img src="lombok/4.jpg" style="width:40%">
    <p>Petak-petak sawah dan kebun-kebun warga yang berwarna-warni dan dikelilingi deretan pegunungan tinggi, ditambah langit biru yang membentang amat sangat menyejukkan mata.<br>
      Berdiri di ketinggian 1.670 meter di atas permukaan laut atau sekitar 673 meter dari dataran Sembalun, Bukit Pergasingan  bisa didaki selama 2 – 2,5 jam dan sering dijadikan tempat latihan sebelum mendaki Gunung Rinjani.<br>
