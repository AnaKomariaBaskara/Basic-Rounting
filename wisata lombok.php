    <h3>4. Pantai Nambung</h3>
    <img src="lombok/8.jpg" style="width:40%">
    <p>Fenomena alam yang cukup unik ini tercipta akibat ombak besar yang menghempas ke dinding batu karang lalu mengucur jatuh kembali ke laut layaknya air terjun.<br>
      Kalau tertarik ingin berselancar, jangan lupa untuk bawa sendiri perlengkapannya karena di sini tak tersedia penyewaan. Perairan di sekitar pantai ini juga sangat jernih sehingga asik untuk berenang.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Dusun Pengantap, Desa Buwun Emas, Kecamatan Sekotong, Lombok Barat<br></div>

    <h3>5.  Batu Payung, Tanjung Aan</h3>
    <img src="lombok/10.jpg" style="width:40%">
    <p>Keberadaan batu menyerupai menara ini terlihat sangat mencolok dibandingkan dengan sekitarnya.<br>
      Batu ini bisa Anda jumpai sekitar 1 kilometer di sebelah timur Tanjung Aan. Sepanjang perjalanan ke sana Anda dapat melihat pemandangan tebing-tebing tinggi yang dihuni oleh burung-burung dan kelelawar.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Jalan Kuta Lombok, Lombok Tengah</div>

    <h3>6. Pantai Tanjung Bloam</h3>
    <img src="lombok/11(1).jpg" style="width:40%">
    <p>Daya tarik utama dari pantai berpasir putih ini adalah batu-batu karang yang ada di sekitarnya, termasuk dua buah tebing karang yang mencuat dan mengapitnya. Kontrasnya warna hitam dari bebatuan, dan warna keemasan dari tebing berpadu dengan birunya laut menghasilkan sebuah komposisi yang sungguh menawan.<br>
    <img src="lombok/11.jpg" style="width:40%"><br>
    Selain bersantai di pinggir pantai, aktivitas seru lainnya adalah fotografi atau menyusuri garis pantainya yang berbatu-batu.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Dusun Tangsi, Desa Temeak, Kecamatan Jerowaru, Lombok Timur</div>

    <h3>7. Pink Beach/Pantai Tangsi</h3>
    <img src="lombok/12.jpg" style="width:40%">
    <p>Sebelum dikenal dengan Pantai Pink, masyarakat menyebutnya Pantai Tangsi. Disebut Pantai Pink karena pasir pantai ini terdiri dari campuran antara pasir putih dan pasir merah yang berasal koral serta pecahan cangkang organisme laut foraminifera yang berwarna kemerahan.<br>
      Waktu paling pas untuk mengunjungi pantai yang tampak seperti dunia Barbie ini adalah dari pagi pukul 08.00 sampai petang sekitar pukul 16.00. Saat itulah bias sinar matahari memantulkan warna merah jambu yang begitu cerah dan cantik.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Desa Sekaroh, Kecamatan Jerowaru, Lombok Timur.</div>

    <h3>8. Air Terjun Penimbungan</h3>
    <img src="lombok/14.jpg" style="width:40%">
    <p>Jalur dari Torean adalah jalan yang jarang dilalui jika dibandingkan dengan pintu masuk Sembalun dan Senaru dalam perjalanan mendaki Rinjani. Bahkan nama Air Terjun Penimbungan sendiri belum banyak yang pernah mendengarnya.<br>
      Setelah menempuh pendakian selama 2 jam dari pintu masuk Torean, Anda akan mencapai spot sempurna untuk menyaksikan air terjun setinggi 75 meter ini. Pemandangan di sekitarnya begitu liar dan alami, persis seperti di masa prasejarah.<br></p>
    
    <h3>9. Air Terjun Benang Kelambu</h3>
    <img src="lombok/16.jpg" style="width:40%">
    <p>Aliran air yang jatuh dari ketinggian tebing tidak langsung terhempas ke bawah, melainkan melewati celah-celah tanaman merambat yang tumbuh lebat di tebing sehingga tampak bagai kelambu atau tirai yang menggantung di udara.<br>
      Jalan menuju air terjun ini memang sudah beraspal, namun sayang kondisinya kurang bagus. Sepanjang perjalanan Anda akan melalui jalan pedesaan yang sedikit berliku dan naik turun dengan hamparan sawah di kiri kanan.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Desa Aik Bukaq, Kecamatan Batukliang Utara, Lombok Tengah</div>

