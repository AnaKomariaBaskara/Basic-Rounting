    <h3>3. Pantai Semeti</h3>
    <img src="lombok/6.jpg" style="width:40%">
    <p>Formasi bebatuan dan karang di sekelilingnya meruncing dan berlapis-lapis layaknya dinding kristal yang terdapat di Panet Krypton. Itulah mengapa pantai ini juga dijuluki Pantai Krypton.<br>
    Jalur menuju ke sini cukup terjal dan berliku, jadi kenakanlah alas kaki anti selip, karena Anda juga harus melewati tebing karang setinggi 20 meter. Dan di balik tebing karang itulah fenomena alam Pantai Semeti yang lain daripada yang lain berada.<br></p>
    <div class="w3-panel w3-leftbar w3-light-grey">Lokasi: Desa Mekar Sari, Kecamatan Praya Barat, Lombok Tengah<br>
