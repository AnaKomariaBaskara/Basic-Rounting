  <h4>6. Villa Sidemen</h4>
  <img src="bali/6.jpg" style="width:40%">
  <p>Anda bisa merasakannya sendiri di Villa Sidemen, di mana setiap tamu yang menginap tak hanya disuguhi panorama gunung berapi tertinggi di Bali, Gunung Agung, namun juga atmosfer yang begitu sunyi dan menenangkan.<br>
    Dan Andapun bisa menikmati pemandangan keren tersebut langsung dari kamar (bahkan dari atas tempat tidur!), karena tiap kamar dilengkapi jendela-jendela besar.<br>
  </p>

  <h4>7. Rumah Pohon Batu Molenteng</h4>
  <img src="bali/7.jpg" style="width:40%">
  <p>Terletak di ujung Nusa Penida, tepatnya menghadap Pantai Atuh – rumah pohon yang indah ini merupakan destinasi kekinian di pulau ini yang belakangan jadi buruan para Instagrammer karena panoramanya yang dramatis.<br>
    Begitu naik ke rumah pohon ini dan melihat pemandangan sekitarnya, Anda pasti bakal dibuat terkesima. Mulai dari Pantai Atuh yang cantik serta pulau-pulau kecil mirip Raja Ampat, sampai tebing-tebing tinggi, batu-batu karang, juga pebukitan yang amat elok!<br>
  </p>

  <h4>8. Desa Temukus</h4>
  <img src="bali/8.jpg" style="width:40%">
  <p>Bunga Marigold atau Gemitir merupakan bunga yang kerap digunakan dalam berbagai ritual adat masyarakat Bali. Dan di sinilah salah satu ladang terbesarnya!<br>
    Desa Temukus merupakan sebuah desa sunyi di kawasan Bali Timur, tak jauh dari pura paling suci umat Hindu Bali, Pura Besakih.<br>
    Di area ini juga terdapat padang Bunga Kasna. Kalau mau ke sini, waktu terbaiknya adalah sebelum musim panen agar Anda bisa menyaksikan lautan bunga-bunga putih yang menyerupai salju!<br>
  </p>

  <h4>9. Pantai Sanur</h4>
  <img src="bali/9.jpg" style="width:40%">
